// Class untuk menyimpan data objek Mahasiswa
public class MahasiswaTugas {
    String nim;
    String nama;
    String telf;

    // Konstruktor untuk menginisialisasi data mahasiswa
    public MahasiswaTugas(String nim, String nama, String telf) {
        this.nim = nim;
        this.nama = nama;
        this.telf = telf;
    }
}