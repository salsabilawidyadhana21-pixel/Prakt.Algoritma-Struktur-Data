// Class untuk menyimpan data objek Mata Kuliah
public class MataKuliahTugas {
    String kode;
    String namaMk;
    int sks;

    // Konstruktor untuk menginisialisasi data mata kuliah
    public MataKuliahTugas(String kode, String namaMk, int sks) {
        this.kode = kode;
        this.namaMk = namaMk;
        this.sks = sks;
    }
}